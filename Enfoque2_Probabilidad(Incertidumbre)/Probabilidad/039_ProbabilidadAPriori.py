# Probabilidad a priori: antes de observar datos
# Supongamos que hay una bolsa con 3 bolas rojas y 2 bolas azules

total_bolas = 5
prob_roja = 3 / total_bolas
prob_azul = 2 / total_bolas

print(f"Probabilidad a priori de sacar una bola roja: {prob_roja}")
print(f"Probabilidad a priori de sacar una bola azul: {prob_azul}")
